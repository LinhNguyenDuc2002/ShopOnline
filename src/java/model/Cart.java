package model;

/**
 *
 * @author thanh
 */
public class Cart {
    private long id;
    
    private long user_id;

    public Cart() {
    }

    public Cart(long id, long user_id) {
        this.id = id;
        this.user_id = user_id;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getUser_id() {
        return user_id;
    }

    public void setUser_id(long user_id) {
        this.user_id = user_id;
    }
    
    
    
}
